function calculateTransform(
    cx: number,
    cy: number,
    svgWidth: number,
    svgHeight: number,
    scaleFactor: number,
) {
    // Calcular o tamanho do SVG após a escala
    const scaledWidth = svgWidth * scaleFactor;
    const scaledHeight = svgHeight * scaleFactor;

    // Encontrar o ponto de origem para que o centro do SVG coincida com o centro do círculo
    const translateX = cx - (scaledWidth / 2);
    const translateY = cy - (scaledHeight / 2);

    // Retorna as coordenadas de translação
    return `translate(${translateX}, ${translateY}) scale(${scaleFactor})`;
}

function Elemento(props: {
    id: string;
    d: string;
    color: string;
    circle_radius: number;
    circle_color: string;
    title: string;
    description: string;
}) {
    const d = props.d;

    const coords = d.replace(/[A-z]/g, " ").split(" ").filter((x) => x !== "")
        .map((x) => parseFloat(x)).filter((x) => x !== 0);

    const [x, y, width, height] = coords;
    const center_x = x + width / 2;
    const center_y = y + height / 2;

    return (
        <>
            <path
                fill={props.color}
                id={props.id}
                d={props.d}
                fill-rule="evenodd"
            />
            <circle
                cx={center_x}
                cy={center_y}
                r={props.circle_radius}
                fill={props.circle_color}
            />
            <g
                transform={calculateTransform(
                    center_x,
                    center_y,
                    200,
                    200,
                    0.2,
                )}
            >
                <Icone />
            </g>
            <text
                x={center_x}
                y={center_y - (props.circle_radius) - 25}
                font-family="Arial"
                font-size="30"
                fill="black"
                text-anchor="middle"
                alignment-baseline="central"
            >
                {props.title}
            </text>
            <text
                x={center_x}
                y={center_y + (props.circle_radius) + 20}
                font-family="Arial"
                font-size="20"
                fill="black"
                text-anchor="middle"
                alignment-baseline="central"
            >
                {props.description}
            </text>

        </>
    );
}

